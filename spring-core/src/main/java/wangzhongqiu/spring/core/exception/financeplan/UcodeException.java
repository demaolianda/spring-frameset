package wangzhongqiu.spring.core.exception.financeplan;

/**
 * U-code使用 exception
 */
public class UcodeException extends RuntimeException {

    private static final long serialVersionUID = -576761749020227512L;

    public UcodeException() {
        super();
    }

    public UcodeException(String arg0, Throwable arg1) {
        super(arg0, arg1);
    }

    public UcodeException(String arg0) {
        super(arg0);
    }

    public UcodeException(Throwable arg0) {
        super(arg0);
    }

}
